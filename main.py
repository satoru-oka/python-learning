# comparison and logical operators
a = 2
b = 2

# when a and b are equal.
print(a == b)

# when a and b are different.
print(a != b)

# a is less than b
print(a < b)

# a is greater than b
print(a > b)

# when a is less than or equal to b
print(a <= b)

# when a is greater than or equal to b
print(a >= b)