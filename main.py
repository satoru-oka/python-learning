# string
print('hello')
print("hello")
print("I don't know")
print('I don\'t know')
# single quote
print('say "I don\'t know"')
# double-quote
print("say \"I dont't know\"")

print('hello.\nHow are you?')
print('C:\name\name')
print(r'C\name\name')

# Whitespace removal at line breaks
print("##########")
print("""
line1
line2
line3
""")
print("##########")
print("""\
line1
line2
line3\
""")
print("##########")

# Output 'Hi.' 3 times
print('Hi.' * 3 + 'Mike.')

# parentheses
s = ('aaaaaaaaaaaaaaaaaaaaaaaaaaaaaa'
     'bbbbbbbbbbbbbbbbbbbbbbbbbbbbbb')

print(s)

# backslash
y = 'aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa' \
    'bbbbbbbbbbbbbbbbbbbbbbbbbbbbbbb'
print(y)